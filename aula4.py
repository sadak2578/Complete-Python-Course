# ========== Tipos primitivos de dados ==========

# o primeiro tipo de dado primitivo é o tipo inteiro

print(12,-12,0,45, sep='|') #este é um tipo de dado primitivo inteiro




# Float -> é um tipo de número com ponto flutuante
# ou seja, é um tipo de dado com casas decimais
print(3.4, 2.45, 20.9823464, 3.1415,sep='|') #float
print(0.0001, -15.786, sep='|') #float

#Função que mostra o tipo de dado em python nós chamamos de Type
print(type('Olá mundo'))
print(type(-23))
print(type(0))
print(type(12.9), type(134.23), type(0.12387267), sep='|')
