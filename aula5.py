# ===================== Valores booleanos =====================
# os valores booleanos são somentes dois, verdadeiros ou falsos
'''
No python temos o que chamamos de operadores lógicos, que são muito utilizados
para questionar coisas dentro do programa.
Dentre esses operadores existe o operador "==" que serve para sabermos se 
alguma variável é igual a outra.
'''
print(10 == 10)
